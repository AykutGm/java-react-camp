package d2hm1;

public class Course {

	int courseId;
	String courseName;
	String courseDetail;
	String courseInstructor;

	public Course(int courseId, String courseName, String courseDetail, String courseInstructor) {
		this.courseId = courseId;
		this.courseName = courseName;
		this.courseDetail = courseDetail;
		this.courseInstructor = courseInstructor;
	}

}
